    private void pay_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pay_btnActionPerformed
        String dbURL = "jdbc:mysql://localhost:3306/pizzacraft"; 
        String dbUsername = "root"; 
        String dbPassword = ""; 
        String paymentMethod = (String) cmb_pay.getSelectedItem(); 

        try (Connection conn = DriverManager.getConnection(dbURL, dbUsername, dbPassword)) {
            // Query to insert payment details
            String query = "INSERT INTO payments (orderId, price, paymentMethod) VALUES (?, ?, ?)";
            PreparedStatement stmt = conn.prepareStatement(query);

            // Parse order ID and amount from the form
            int orderId = Integer.parseInt(order_paytxt.getText());
            double price = Double.parseDouble(price_paytxt.getText().replace("$", ""));

            // Set the parameters for the query
            stmt.setInt(1, orderId);
            stmt.setDouble(2, price);
            stmt.setString(3, paymentMethod);

            // Execute the update and check if rows were inserted
            int rowsInserted = stmt.executeUpdate();
            if (rowsInserted > 0) {
                JOptionPane.showMessageDialog(this, "Payment Successful!");
                dispose(); // Close the payment form
            } else {
                JOptionPane.showMessageDialog(this, "Payment failed. Please try again.");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Payment Failed: " + ex.getMessage());
        }

    }//GEN-LAST:event_pay_btnActionPerformed

