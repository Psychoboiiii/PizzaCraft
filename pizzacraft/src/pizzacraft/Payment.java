    private void fetchOrderDetails() {
        String dbURL = "jdbc:mysql://localhost:3306/pizzacraft";
        String dbUsername = "root";
        String dbPassword = "";

        try (Connection conn = DriverManager.getConnection(dbURL, dbUsername, dbPassword)) {
            // Query to fetch the latest order details
            String query = "SELECT orderId, price FROM orderplaced ORDER BY orderId DESC LIMIT 1";
            PreparedStatement stmt = conn.prepareStatement(query);

            ResultSet rs = stmt.executeQuery();
            if (rs.next()) {
                int orderId = rs.getInt("orderId");
                double price = rs.getDouble("price");

                // Display the fetched details in the form
                order_paytxt.setText(String.valueOf(orderId));
                price_paytxt.setText(String.format("$%.2f", price));
            } else {
                JOptionPane.showMessageDialog(this, "No orders found.");
                dispose(); // Close the payment form if no orders exist
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error fetching order details: " + ex.getMessage());
        }
    }

